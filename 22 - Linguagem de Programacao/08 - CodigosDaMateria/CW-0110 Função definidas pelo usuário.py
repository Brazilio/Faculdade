# Funções, também conhecidas como subprogramas ou sub-rotinas, são pequenos blocos de 
# código aos quais se dá um nome, desenvolvidos para resolver tarefas específicas.
'''A sintaxe de uma função em Python é feita com:
A palavra reservada def.
O nome da função.
Os parênteses que indicam se existem ou não parâmetros para a função.
E o comando return (que é opcional);'''

