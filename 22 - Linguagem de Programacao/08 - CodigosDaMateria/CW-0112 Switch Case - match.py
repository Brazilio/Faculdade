# Similar ao Switch case o Python possui o match case

estacao = "primavera"

match estacao:
    
    case "primavera":
        print("Estação das Flores")
    case "verao":
        print("O Verao é quente")
    case "inverno":
        print("Estação das Flores")
    case "verao":
        print("O outono é sempre igual, as floques caem no quintal")
        